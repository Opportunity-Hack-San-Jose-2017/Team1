package gov.sanjoseca.programs.walknroll.internal.rest;

import gov.sanjoseca.programs.walknroll.format.ISO8601Date;
import gov.sanjoseca.programs.walknroll.format.ISO8601DateConverter;

import javax.ws.rs.ext.ParamConverter;
import javax.ws.rs.ext.ParamConverterProvider;
import javax.ws.rs.ext.Provider;
import java.lang.annotation.Annotation;
import java.lang.reflect.Type;

@Provider
public class ISO8601DateConverterProvider implements ParamConverterProvider {

    @Override
    public <T> ParamConverter<T> getConverter(Class<T> rawType, Type genericType, Annotation[] annotations) {
        if (ISO8601Date.class.isAssignableFrom(rawType)) {
            return (ParamConverter<T>) new ISO8601DateConverter();
        }
        return null;
    }
}
